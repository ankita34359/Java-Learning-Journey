import java.util.*;

public class SawpNumber {

    public static void main(String[] args) {

        int a = 5;
        int b = 6;
        int c;

        // Before swapping

        System.out.println("b=" + a);
        System.out.println("a=" + b);

        c=a;
        a=b;

        // After swapping

        System.out.println("a=" + c);
        System.out.println("b=" + a);
        
        
    }
    
}
